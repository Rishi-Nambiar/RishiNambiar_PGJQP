#include<iostream>
using namespace std;

int main() 
{
    int intType;
    float floatType;
    double doubleType;
    char charType;

    cout<<"The size of int data type is: "<<sizeof(intType)<<" bytes"<<'\n';
    cout<<"The size of float data type is: "<<sizeof(floatType)<<" bytes"<<'\n';
    cout<<"The size of double data type is: "<<sizeof(doubleType)<<" bytes"<<'\n';
    cout<<"The size of char data type is: "<<sizeof(charType)<<" bytes"<<'\n';
}

